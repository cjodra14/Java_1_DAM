package ventas;

public class Venta {
	
	private int vendedor;
	private int prod;
	private int kilos;
	
	public int getVendedor() {
		return vendedor;
	}
	public void setVendedor(int vendedor) {
		this.vendedor = vendedor;
	}
	public int getProd() {
		return prod;
	}
	public void setProd(int prod) {
		this.prod = prod;
	}
	public int getKilos() {
		return kilos;
	}
	public void setKilos(int kilos) {
		this.kilos = kilos;
	}

}
